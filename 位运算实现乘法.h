//俄罗斯农民乘法：
//原理是将被乘数除以2, 乘数乘以2, 如果被乘数除以2以后有余数(余数为1), 则将乘数（本轮乘2之前的值）累加到结果中.
//那么乘除2都可以用位移运算

int quickMulti(int A, int B) {
    int ans = 0;
    for (; B; B >>= 1) {        //右移1位等价于除以2
        if (B & 1) {            //余数是否为1就看最低位是不是1即可
            ans += A;
        }
        A <<= 1;                //左移1位等价于乘2
    }
    return ans;
}
